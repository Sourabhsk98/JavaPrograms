package com.TodaysJavaPrograms;

 /* Java program to demonstrate working
               of java.lang.Math.sqrt() method */
public class SquareRoot2 {
    public static void main(String args[])
    {
        int a = 100;

        System.out.println(Math.sqrt(a));

        a = 25;

        System.out.println(Math.sqrt(a));

        a = 64;

        System.out.println(Math.sqrt(a));

        a = 90;

        System.out.println(Math.sqrt(a));
    }
}